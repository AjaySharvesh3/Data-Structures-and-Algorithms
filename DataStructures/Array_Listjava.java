import java.util.*;  
class ArrayList3{  
 public static void main(String args[]){  
  ArrayList<String> al=new ArrayList<String>();  
  al.add("lekha");  
  al.add("divi");  
  al.add("sri");  
  al.add("ishu");  
   //Traversing list through for-each loop  
  for(String obj:al)  
    System.out.println(obj);  
 }  
}  